console.log(escola.toLowerCase()) //Deixa todas as letras minusculas
console.log(escola.length()) // Conta quastos caracteres tem esse texto
console.log(escola.includes("i")) // Procura um caractere especifico e se ele encontra retorna como verdadeiro, senão retorna falso

